		<h2>Translit</h2>
		 <p>Translit is a service that consists in writing in cyrillic using the equivalent latin letters or combinations. (See Fig. 2)</p>
		 <img width="750" src="images/guide/translitHelp.png"/><br/>
		<small>Fig 2. Translit overview</small>
		 <ul>
			<li><b>1.</b> Toggle output language. <i>РУС</i> for Russian transliteration and <i>ABC</i> for English</li>
			<li><b>2.</b> Toggle helper keyboard. The helper keyboard allows you to input Cyrillic characters with your mouse.</li>
			<li><b>3.</b> Select all text</li>
			<li><b>4.</b> Convert selected text from Latin to Cyrillic</li>
			<li><b>5.</b> Convert selected text from Cyrillic to Latin</li>
			<li><b>6.</b> Select external service to query with the current text</li>
			<li><b>7.</b> Performs external query (opens in a new tab)</li>
			<li><b>8.</b> Number of characters in current text</li>
			<li><b>9.</b> Transliteration table with equivalent letters and combinations</li>
			<li><b>10.</b> Toggle upper/lower-case in the transliteration table</li>
		 </ul>
		 <br/><br/>
		 <h2>Keyboard emulator</h2>
		 <p>The Keyboard emulator simulates an actual Russian keyboad layout. Instead of writing an equivalent letter, you must press the correspondent key in the Russian keyboard. (See Fig. 3)</p>
		 <img width="750" src="images/guide/keyboardHelp.png"/><br/>
		<small>Fig 3. Keyboard emulator overview</small>
		 <ul>
			<li><b>1.</b> Toggle output keyboard. <i>РУС</i> for Russian keyboard and <i>ABC</i> for standard keyboard layout</li>
			<li><b>2.</b> Select all text</li>
			<li><b>3.</b> Select input keyboard language (some letters may have different positioning depending on the input language, such as Y/Z in german or M/A/Q/ in  French</li>
			<li><b>4.</b> Keyboard icon. Decorative purpose.</li>
			<li><b>5.</b> Select external service to query with the current text</li>
			<li><b>6.</b> Performs external query (opens in a new tab)</li>
			<li><b>7.</b> Number of characters in current text</li>
			<li><b>8.</b> Helper keyboard for the current entry language</li>
			<li><b>9.</b> Same as 1.</li>
		 </ul>
		 
		 
		 		 
		 	
