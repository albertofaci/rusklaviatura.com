function preview_select() { 
	var excerpt = $("#textito_dec").val().substr(0, 64);
	var options = $("#decoding_preview");
	
	//options.children().remove().end();
	
	var previews = new Array();
	m = 0;
	for(j=0; j<=4; j++) {
		for(k=0; k<=4; k++) {
			if(j==k) continue;
			previews[m] = decodeText(excerpt,j,k);
			options.append($("<option />").val(j+"_"+k).text(previews[m]));
			m++;
		}
	}
	
}

function decodeText(text, from, to) {
	var ret = "";
	for(i=0; i<text.length; i++) {
		var c = text.charAt(i)
		if(s0[c]){
			var ord = s0[c]
			var from_code = c1[from][ord];
			var to_code = c2[to][from_code];
			if(to_code) {
				ret = ret+s1[to_code];
			}
			else {
				ret = ret+"?";	
			}
		}
		else {
			ret = ret+c;
		}
	}
	return ret;
}

